// Quick access array of just emojis for random selection
export const GOAL_ICONS = SEARCHABLE_ICONS.map(icon => icon.emoji);

// Get a random icon from the set
export const getRandomIcon = (): string => {
  const index = Math.floor(Math.random() * GOAL_ICONS.length);
  return GOAL_ICONS[index];
};

// Get the default icon (first in list)
export const getDefaultIcon = (): string => {
  return GOAL_ICONS[0];
};

// Search icons by keyword
export const searchIcons = (query: string): string[] => {
  if (!query.trim()) return GOAL_ICONS;

  const lowerQuery = query.toLowerCase().trim();

  return SEARCHABLE_ICONS
    .filter(icon =>
      icon.keywords.some(keyword => keyword.includes(lowerQuery)) ||
      icon.emoji === query
    )
    .map(icon => icon.emoji);
};
